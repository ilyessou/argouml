package org.omg.uml.foundation.datatypes;

/**
 * TimeExpression class proxy interface.
 */
public interface TimeExpressionClass extends javax.jmi.reflect.RefClass {
    /**
     * The default factory operation used to create an instance object.
     * @return The created instance object.
     */
    public TimeExpression createTimeExpression();
    /**
     * Creates an instance object having attributes initialized by the passed 
     * values.
     * @param language 
     * @param body 
     * @return The created instance object.
     */
    public TimeExpression createTimeExpression(java.lang.String language, java.lang.String body);
}
