package org.omg.uml.foundation.datatypes;

/**
 * MappingExpression class proxy interface.
 */
public interface MappingExpressionClass extends javax.jmi.reflect.RefClass {
    /**
     * The default factory operation used to create an instance object.
     * @return The created instance object.
     */
    public MappingExpression createMappingExpression();
    /**
     * Creates an instance object having attributes initialized by the passed 
     * values.
     * @param language 
     * @param body 
     * @return The created instance object.
     */
    public MappingExpression createMappingExpression(java.lang.String language, java.lang.String body);
}
