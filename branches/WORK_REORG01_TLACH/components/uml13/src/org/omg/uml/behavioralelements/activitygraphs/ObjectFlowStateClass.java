package org.omg.uml.behavioralelements.activitygraphs;

/**
 * ObjectFlowState class proxy interface.
 */
public interface ObjectFlowStateClass extends javax.jmi.reflect.RefClass {
    /**
     * The default factory operation used to create an instance object.
     * @return The created instance object.
     */
    public ObjectFlowState createObjectFlowState();
    /**
     * Creates an instance object having attributes initialized by the passed 
     * values.
     * @param name 
     * @param visibility 
     * @param isSpecification 
     * @param isSynch 
     * @return The created instance object.
     */
    public ObjectFlowState createObjectFlowState(java.lang.String name, org.omg.uml.foundation.datatypes.VisibilityKind visibility, boolean isSpecification, boolean isSynch);
}
